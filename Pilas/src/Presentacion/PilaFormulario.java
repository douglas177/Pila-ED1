    private void ETBitsKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ETBitsKeyReleased
        if (ETBits.getText().matches("[0-9]*") && (ETBits.getText().length() != 0)) {
            if (Integer.valueOf(ETBits.getText()) != 0) {
                BotonBits = true;
            } else {
                BotonBits = false;
            }

        } else {
            BotonBits = false;
            ETBits.setText("");
        }
        HabilitarBits();
    }//GEN-LAST:event_ETBitsKeyReleased

