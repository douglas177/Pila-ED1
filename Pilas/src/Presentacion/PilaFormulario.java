    private void PushBitsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PushBitsActionPerformed
        int x = Integer.valueOf(ElementoBits.getText());
        if (!PBits.Llena()&& x<Math.pow(2,PBits.Nbit())) {  
            PBits.Push(x);
            AreaBits.setText(PBits.toString());
            ElementoBits.setText("");
        } else {
            if(x>=Math.pow(2,PBits.Nbit())){
                int y = (int) (Math.pow(2, PBits.Nbit()) -1);
                JOptionPane.showMessageDialog(rootPane, "El Valor MAXIMO debe ser de " + y, "Estructura de Datos 1", 1);
            }
            else
            JOptionPane.showMessageDialog(rootPane, "Pila Llena", "Estructura de Datos 1", 1);
  ElementoBits.setText("");
        }
        PushBits.setEnabled(false);
    }//GEN-LAST:event_PushBitsActionPerformed

